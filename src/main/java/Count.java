public class Count {

    private String string;
    private int length;

    public Count(String string, int length) {
        this.string = string;
        this.length = length;
    }

    public String getString() {
        return string;
    }

    public void setString(String string) {
        this.string = string;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
